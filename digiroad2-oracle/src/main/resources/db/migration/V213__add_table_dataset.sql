CREATE TABLE MUNICIPALITY_DATASET (
 DATASET_ID CHAR(36) PRIMARY KEY,
 GEOJSON CLOB NOT NULL,
 ROADLINKS CLOB NOT NULL,
 RECEIVED_DATE TIMESTAMP DEFAULT CURRENT_TIMESTAMP NOT NULL,
 PROCESSED_DATE TIMESTAMP,
 STATUS NUMBER
);

CREATE TABLE MUNICIPALITY_FEATURE (
 FEATURE_ID NUMBER,
 DATASET_ID CHAR(36) REFERENCES MUNICIPALITY_DATASET(DATASET_ID),
 STATUS VARCHAR2(4000),
 CONSTRAINT PK_MUNICIPALITY_FEATURE PRIMARY KEY(FEATURE_ID, DATASET_ID)
);